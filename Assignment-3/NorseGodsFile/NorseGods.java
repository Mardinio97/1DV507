package km222pw_assign3.NorseGodsFile;

public class NorseGods {
    private String name;
    private String race;
    private String desc;

    public NorseGods() {
    }

    public NorseGods(String name, String race, String desc) {
        this.name = name;
        this.race = race;
        this.desc = desc;
    }

    public String getName() {
        return name;
    }


    public String getRace() {
        return race;
    }

    public String getDesc() {
        return desc;
    }



}
